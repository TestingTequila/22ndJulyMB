package _3loops;

public class _2LearningForLoop {

	public static void main(String[] args) {

		// WAP to print numbers from 1 to 10;

		for (int num1 = 1; num1 < 11; num1++) {
			System.out.println(num1);
		}

		System.out.println("=================================================");

		// WAP to print numbers from 10 to 1;

		for (int num2 = 10; num2 > 0; num2--) {
			System.out.println(num2);
		}

		// WAP to print table of 2
		System.out.println("=================================================");
		
		for (int x = 1;x < 11;x++) 
		{
			System.out.println(2 * x);
			
		}

	}

}
